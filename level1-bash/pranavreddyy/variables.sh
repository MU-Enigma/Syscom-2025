#!/bin/bash

# This script demonstrates how to declare and use a variable.

# 1. Declare a variable named 'NAME' and assign it the string "Ankita".
#    Note: There are no spaces around the equals sign!
NAME="Ankita"

# 2. Use the variable by putting a '$' in front of its name.
#    This tells Bash to replace the variable's name with its stored value.
echo "Hello, $NAME!"